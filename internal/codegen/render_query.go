package codegen

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"text/template"
)

type queryMethod struct {
	Name      string
	ConstName string
	SQL       string
	IsExec    bool
	IsOne     bool
	IsMany    bool
	Args      []goField // param args (Name + Type)
	ArgNames  []string
	RowType   string
	ScanList  []string
}

type queriesFile struct {
	Package    string
	Imports    []string
	RowStructs []goStruct
	Methods    []queryMethod
}

// RenderQueries generates the queries file (a Querier with typed methods) for
// the analyzed queries. Output is go/format-clean (and thus valid Go syntax).
func RenderQueries(cat *Catalog, queries []Query, pkg string) ([]byte, error) {
	qf := queriesFile{Package: pkg}
	importSet := map[string]struct{}{"context": {}, "database/sql": {}}

	for _, q := range queries {
		m := queryMethod{
			Name:      q.Name,
			ConstName: lowerFirst(q.Name),
			SQL:       q.SQL,
			IsExec:    q.Cmd == CmdExec,
			IsOne:     q.Cmd == CmdOne,
			IsMany:    q.Cmd == CmdMany,
		}
		for _, p := range q.Params {
			if p.GoType.Import != "" {
				importSet[p.GoType.Import] = struct{}{}
			}
			m.Args = append(m.Args, goField{Name: p.Name, Type: p.GoType.Name})
			m.ArgNames = append(m.ArgNames, p.Name)
		}
		if q.Cmd != CmdExec {
			if q.Star {
				m.RowType = structName(q.Table)
			} else {
				m.RowType = q.Name + "Row"
				rs := goStruct{Name: m.RowType, Table: q.Table}
				for _, col := range q.Columns {
					gt := goTypeFor(col)
					if gt.Import != "" {
						importSet[gt.Import] = struct{}{}
					}
					rs.Fields = append(rs.Fields, goField{
						Name: goName(col.Name),
						Type: gt.Name,
						Tag:  fmt.Sprintf("`json:%q db:%q`", col.Name, col.Name),
					})
				}
				qf.RowStructs = append(qf.RowStructs, rs)
			}
			for _, col := range q.Columns {
				m.ScanList = append(m.ScanList, "&i."+goName(col.Name))
			}
		}
		qf.Methods = append(qf.Methods, m)
	}
	for imp := range importSet {
		qf.Imports = append(qf.Imports, imp)
	}
	sort.Strings(qf.Imports)

	var buf bytes.Buffer
	if err := queriesTmpl.Execute(&buf, qf); err != nil {
		return nil, fmt.Errorf("render queries: %w", err)
	}
	out, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format queries: %w\n--- raw ---\n%s", err, buf.String())
	}
	return out, nil
}

